#Lesson 05: Control Flow & Loops

# If-Else Example
age = 18
if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# For Loop Example
for i in range(3):
    print("Hello!")

# While Loop Example
count = 0
while count < 2:
    print("Counting...", count)
    count += 1


